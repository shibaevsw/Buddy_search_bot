# Идеографический пробел. Выглядит как обычный, но это другой символ.
# Он имеет большую фиксированную ширину.
_IDEOGRAPHIC_SPACE = '　'

def as_full_width(text: str) -> str:
    """
    Растягивает блок сообщения на всю ширину, добавляя в конец
    несколько широких неразрывных пробелов.
    """
    if not isinstance(text, str):
        text = str(text)

    # Добавляем несколько (например, 10) широких пробелов.
    # Этого количества достаточно, чтобы растянуть блок,
    # но слишком мало, чтобы они перенеслись на новую строку.
    # Они просто обрежутся клиентом, если не поместятся.
    filler = _IDEOGRAPHIC_SPACE * 50

    return f"{text}{filler}"
